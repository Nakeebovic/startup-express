import helmet from 'helmet';

export const helmetMiddleware = (options?: Parameters<typeof helmet>[0]) => {
  return helmet({
    contentSecurityPolicy: {
      directives: {
        defaultSrc: ["'self'"],
        styleSrc: ["'self'", "'unsafe-inline'"],
        scriptSrc: ["'self'"],
        imgSrc: ["'self'", 'data:', 'https:'],
      },
    },
    ...options,
  });
};

